#include "Includes/libft.h"

int		ft_is_number(char *str)
{
	int		i;

	i = 0;
	if (!str)
		return (0);
	while (str[i])
	{
		if ((str[i] == '-' && i > 0) || (!ft_isdigit(str[i]) && str[i] != '-'))
			return (0);
		i++;
	}
	return (1);
}
